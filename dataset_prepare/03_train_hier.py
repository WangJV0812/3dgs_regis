#!/usr/bin/env python
"""
Step 3: Train HIER 3DGS from COLMAP/VGGT reconstruction

This script trains a Hierarchical 3D Gaussian Splatting model from the sparse
reconstruction generated by COLMAP (Step 1) or VGGT (Step 2).

Key features:
- Single-scene training (no chunking required)
- Directly uses VGGT poses and intrinsics for alignment
- Optionally builds hierarchy (.hier) from trained PLY if
  GaussianHierarchyCreator is available
- Falls back to standard PLY output if hierarchy tools are not compiled

Usage:
    # Train from COLMAP
    python 03_train_hier.py --scene_dir /path/to/scene --source colmap \
        --iterations 30000

    # Train from VGGT (no chunking, uses VGGT camera poses directly)
    python 03_train_hier.py --scene_dir /path/to/scene --source vggt \
        --iterations 30000

    # Train + build hierarchy + post-optimize
    python 03_train_hier.py --scene_dir /path/to/scene --source vggt \
        --iterations 30000 --build_hierarchy --post_opt
"""

import argparse
import os
import sys
import subprocess
import shutil
import platform
from pathlib import Path
import json

# --------------------------------------------------------------------------- #
# Environment bootstrap                                                       #
# --------------------------------------------------------------------------- #
# Some locally-compiled CUDA extensions (e.g., diff_gaussian_rasterization)
# were built against a newer libstdc++ than the system provides.  Ensure the
# Conda environment's lib directory appears in LD_LIBRARY_PATH so that both
# this process and every subprocess we spawn can resolve the correct library.
# --------------------------------------------------------------------------- #

def _ensure_conda_lib_in_ld_path():
    prefix = os.environ.get("CONDA_PREFIX")
    if not prefix:
        prefix = os.path.dirname(os.path.dirname(sys.executable))
    lib_dir = os.path.join(prefix, "lib")
    if os.path.isdir(lib_dir):
        ld_path = os.environ.get("LD_LIBRARY_PATH", "")
        if lib_dir not in ld_path.split(os.pathsep):
            os.environ["LD_LIBRARY_PATH"] = (
                f"{lib_dir}:{ld_path}" if ld_path else lib_dir
            )

_ensure_conda_lib_in_ld_path()

# --------------------------------------------------------------------------- #
# Paths                                                                       #
# --------------------------------------------------------------------------- #

HIER_3DGS_PATH = Path(__file__).parent.parent / "submodule" / "hier_3dgs"
sys.path.insert(0, str(HIER_3DGS_PATH))

# --------------------------------------------------------------------------- #
# Utilities                                                                   #
# --------------------------------------------------------------------------- #

def run_command(cmd: str, cwd: str | None = None, check: bool = True):
    """Run a shell command, streaming stdout/stderr back."""
    print(f"Running: {cmd}")
    result = subprocess.run(cmd, shell=True, cwd=cwd,
                            capture_output=True, text=True)
    if result.stdout:
        print(result.stdout)
    if result.stderr:
        print(result.stderr, file=sys.stderr)
    if check and result.returncode != 0:
        raise RuntimeError(
            f"Command failed with return code {result.returncode}"
        )
    return result


def find_hierarchy_creator() -> str | None:
    """Return the path to GaussianHierarchyCreator if it exists."""
    is_win = platform.system() == "Windows"
    binary_name = (
        "GaussianHierarchyCreator.exe" if is_win else "GaussianHierarchyCreator"
    )
    rel_build = (
        "submodules/gaussianhierarchy/build/Release/GaussianHierarchyCreator.exe"
        if is_win
        else "submodules/gaussianhierarchy/build/GaussianHierarchyCreator"
    )
    candidate = HIER_3DGS_PATH / rel_build
    if candidate.exists():
        return str(candidate)
    for path_dir in os.environ.get("PATH", "").split(os.pathsep):
        fallback = Path(path_dir) / binary_name
        if fallback.exists():
            return str(fallback)
    return None


def _write_train_test_split(sparse_dir: Path, image_names: list[str]):
    """Generate test.txt / train.txt inside sparse_dir for --eval mode."""
    if not image_names:
        return
    test_names = [name for idx, name in enumerate(image_names) if idx % 8 == 0]
    train_names = [name for name in image_names if name not in test_names]
    (sparse_dir / "test.txt").write_text("\n".join(test_names) + "\n")
    (sparse_dir / "train.txt").write_text("\n".join(train_names) + "\n")
    print(f"Created train/test split: {len(train_names)} train, "
          f"{len(test_names)} test")


def prepare_dataset_structure(scene_dir: Path, source: str):
    """
    Ensure the source_result_dir matches HIER 3DGS expectations.
    Returns (source_result_dir, sparse_dir).
    """
    input_dir = scene_dir / "input" / "images"
    source_result_dir = scene_dir / f"{source}_result"
    sparse_dir = source_result_dir / "sparse"

    if not input_dir.exists():
        raise FileNotFoundError(f"Input images not found: {input_dir}")
    if not sparse_dir.exists():
        raise FileNotFoundError(
            f"{source.upper()} reconstruction not found: {sparse_dir}\n"
            f"Please run Step {'1' if source == 'colmap' else '2'} first."
        )

    # Symlink images so train_single.py can find them
    images_link = source_result_dir / "images"
    if not images_link.exists():
        print(f"Creating symlink: {images_link} -> {input_dir}")
        images_link.symlink_to(input_dir.resolve())

    # train_single.py looks for COLMAP data under sparse/0/
    sparse_zero = sparse_dir / "0"
    if not sparse_zero.exists():
        has_colmap_files = all(
            (sparse_dir / f).exists()
            for f in ("cameras.bin", "images.bin", "points3D.bin")
        )
        if has_colmap_files:
            print(f"Creating symlink: {sparse_zero} -> {sparse_dir}")
            sparse_zero.symlink_to(sparse_dir.resolve())

    # Always regenerate train/test split based on the actual reconstructed images
    # (VGGT may only use a subset of input/images).
    try:
        from scene.colmap_loader import read_extrinsics_binary
        cam_extrinsics = read_extrinsics_binary(str(sparse_dir / "images.bin"))
        image_names = sorted(cam_extrinsics[k].name for k in cam_extrinsics)
    except Exception as exc:
        print(f"Warning: could not read images.bin ({exc}); "
              "falling back to input_dir listing.")
        image_names = sorted(
            p.name for p in input_dir.iterdir()
            if p.suffix.lower() in (".jpg", ".jpeg", ".png", ".bmp")
        )
    _write_train_test_split(sparse_dir, image_names)

    return source_result_dir, sparse_dir


def _build_hierarchy(
    ply_path: Path,
    source_path: Path,
    output_dir: Path,
    scaffold_dir: Path | None = None,
) -> Path:
    """Build hierarchy.hier from a trained PLY using GaussianHierarchyCreator."""
    creator = find_hierarchy_creator()
    if not creator:
        raise FileNotFoundError(
            "GaussianHierarchyCreator not found.\n"
            "Please compile the gaussianhierarchy submodule:\n"
            "  cd submodule/hier_3dgs/submodules/gaussianhierarchy\n"
            "  mkdir build && cd build && cmake .. && make -j"
        )

    args = [str(creator), str(ply_path), str(source_path), str(output_dir)]
    if scaffold_dir and scaffold_dir.exists():
        args.append(str(scaffold_dir))

    run_command(" ".join(args))

    hier_path = output_dir / "hierarchy.hier"
    if not hier_path.exists():
        raise FileNotFoundError(
            f"Hierarchy creation failed: {hier_path} not found"
        )
    print(f"  Created hierarchy: {hier_path}")
    return hier_path


def post_optimize_hierarchy(
    source_path: Path,
    model_dir: Path,
    iterations: int = 15000,
    disable_viewer: bool = True,
) -> Path:
    """Run train_post.py to refine the hierarchy."""
    train_post = HIER_3DGS_PATH / "train_post.py"
    if not train_post.exists():
        raise FileNotFoundError(f"train_post.py not found: {train_post}")

    hier_path = model_dir / "hierarchy.hier"
    if not hier_path.exists():
        raise FileNotFoundError(
            f"Hierarchy file not found for post-opt: {hier_path}"
        )

    parts = [
        f"python {train_post}",
        f"-s {source_path}",
        f"-m {model_dir}",
        f"--iterations {iterations}",
        "--feature_lr 0.0005",
        "--opacity_lr 0.01",
        "--scaling_lr 0.001",
        "--save_iterations -1",
        f"--hierarchy {hier_path}",
    ]
    if disable_viewer:
        parts.append("--disable_viewer")
    cmd = " ".join(parts)

    print("\n[Hierarchy] Running post-optimization on hierarchy...")
    run_command(cmd, cwd=str(HIER_3DGS_PATH))

    opt_hier = model_dir / "hierarchy.hier_opt"
    if opt_hier.exists():
        print(f"  Post-optimized hierarchy: {opt_hier}")
    return opt_hier


def train_hier_3dgs(
    scene_dir: Path,
    source: str,
    iterations: int = 30000,
    save_iterations=None,
    checkpoint_iterations=None,
    use_depth: bool = False,
    depth_weight_init: float = 0.1,
    depth_weight_final: float = 0.01,
    disable_viewer: bool = True,
    build_hierarchy: bool = False,
    post_opt: bool = False,
):
    """Train HIER 3DGS model."""
    source_path, _ = prepare_dataset_structure(scene_dir, source)

    result_dir = scene_dir / "3dgs_result"
    model_dir = result_dir / "model"
    ckpt_dir = model_dir / "ckpt"
    log_dir = result_dir / "log"

    result_dir.mkdir(parents=True, exist_ok=True)
    model_dir.mkdir(parents=True, exist_ok=True)
    ckpt_dir.mkdir(parents=True, exist_ok=True)
    log_dir.mkdir(parents=True, exist_ok=True)

    if save_iterations is None:
        save_iterations = [7000, 15000, iterations]
    else:
        save_iterations = sorted(set(save_iterations + [iterations]))

    if checkpoint_iterations is None:
        checkpoint_iterations = []

    print(f"\n{'='*70}")
    print("HIER 3DGS Training")
    print(f"{'='*70}")
    print(f"Scene: {scene_dir}")
    print(f"Source: {source.upper()}")
    print(f"Iterations: {iterations}")
    print(f"Save at: {save_iterations}")
    print(f"Build hierarchy: {build_hierarchy}")
    print(f"Post-opt hierarchy: {post_opt}")
    print(f"Output: {result_dir}")

    if build_hierarchy:
        creator = find_hierarchy_creator()
        if creator:
            print(f"✓ GaussianHierarchyCreator found: {creator}")
        else:
            print("⚠ GaussianHierarchyCreator not found. "
                  "Will fall back to PLY output.")

    train_script = HIER_3DGS_PATH / "train_single.py"
    if not train_script.exists():
        raise FileNotFoundError(f"Training script not found: {train_script}")

    cmd_parts = [
        f"python {train_script}",
        f"-s {source_path}",
        f"-m {model_dir}",
        f"--iterations {iterations}",
        f"--save_iterations {' '.join(map(str, save_iterations))}",
    ]
    if checkpoint_iterations:
        cmd_parts.append(
            f"--checkpoint_iterations {' '.join(map(str, checkpoint_iterations))}"
        )
    if use_depth:
        cmd_parts.extend([
            f"--depth_l1_weight_init {depth_weight_init}",
            f"--depth_l1_weight_final {depth_weight_final}",
        ])
    if disable_viewer:
        cmd_parts.append("--disable_viewer")
    cmd_parts.append("--eval")

    print("\n[1/2] Starting 3DGS training...")
    run_command(" ".join(cmd_parts), cwd=str(HIER_3DGS_PATH))

    final_ply = model_dir / f"point_cloud/iteration_{iterations}/point_cloud.ply"
    if not final_ply.exists():
        raise FileNotFoundError(
            f"Expected PLY not found after training: {final_ply}"
        )
    print(f"  Trained PLY: {final_ply}")

    hierarchy_built = False
    final_hier_path = None

    if build_hierarchy and find_hierarchy_creator():
        try:
            hier_path = _build_hierarchy(final_ply, source_path, model_dir)
            hierarchy_built = True
            if post_opt:
                final_hier_path = post_optimize_hierarchy(
                    source_path, model_dir, iterations=15000,
                    disable_viewer=disable_viewer
                )
            else:
                final_hier_path = hier_path
        except Exception as exc:
            print(f"\n⚠ Hierarchy build failed: {exc}", file=sys.stderr)
            print("  Falling back to PLY output.")

    final_dest = model_dir / "final.hier"
    if hierarchy_built and final_hier_path and final_hier_path.exists():
        shutil.copy2(final_hier_path, final_dest)
        print(f"  Copied final hierarchy to: {final_dest}")
    else:
        stub_path = model_dir / "final.json"
        stub_path.write_text(
            json.dumps({
                "format": "ply",
                "ply_path": str(final_ply),
                "note": "Hierarchy tools not available; output is standard 3DGS PLY",
                "hierarchy_available": False,
            }, indent=2)
        )
        print(f"  Saved fallback metadata to: {stub_path}")
        print(f"  (Use PLY directly: {final_ply})")

    config = {
        "source": source,
        "iterations": iterations,
        "save_iterations": save_iterations,
        "use_depth": use_depth,
        "depth_weight_init": depth_weight_init,
        "depth_weight_final": depth_weight_final,
        "source_path": str(source_path),
        "model_path": str(model_dir),
        "hierarchy_built": hierarchy_built,
        "post_optimized": post_opt and hierarchy_built,
    }
    config_path = result_dir / "config.json"
    config_path.write_text(json.dumps(config, indent=2))
    print(f"  Saved config to: {config_path}")

    print(f"\n{'='*70}")
    print("HIER 3DGS Training Complete!")
    print(f"{'='*70}")
    print(f"Model directory: {model_dir}")
    print(f"  - point_cloud/iteration_{iterations}/point_cloud.ply: "
          "Standard 3DGS model")
    if hierarchy_built:
        print("  - hierarchy.hier: Hierarchical model")
        if post_opt:
            print("  - hierarchy.hier_opt: Post-optimized hierarchical model")
        print("  - final.hier: Final hierarchy output")
    else:
        print("  - final.json: Fallback metadata")
        print("\nTo enable hierarchy generation, compile gaussianhierarchy:")
        print(f"  cd {HIER_3DGS_PATH}/submodules/gaussianhierarchy")
        print("  mkdir build && cd build && cmake .. && make -j")

    return model_dir


def validate_source(scene_dir: Path, source: str):
    """Validate that source reconstruction exists and is valid."""
    source_result_dir = scene_dir / f"{source}_result"
    sparse_dir = source_result_dir / "sparse"

    if not source_result_dir.exists():
        raise FileNotFoundError(
            f"{source.upper()} result directory not found: {source_result_dir}\n"
            f"Please run Step {'1' if source == 'colmap' else '2'} first."
        )
    if not sparse_dir.exists():
        raise FileNotFoundError(
            f"Sparse reconstruction not found: {sparse_dir}"
        )

    for fname in ("cameras.bin", "images.bin", "points3D.bin"):
        fpath = sparse_dir / fname
        if not fpath.exists():
            raise FileNotFoundError(f"Missing required file: {fpath}")

    print(f"✓ Valid {source.upper()} reconstruction found")
    print("  - cameras.bin: Camera intrinsics")
    print("  - images.bin: Camera poses")
    print("  - points3D.bin: Sparse 3D points")
    return True


def main():
    parser = argparse.ArgumentParser(
        description="Train HIER 3DGS from COLMAP or VGGT"
    )
    parser.add_argument(
        "--scene_dir", type=str, required=True,
        help="Scene directory containing input/images/ and "
             "[colmap|vggt]_result/sparse/"
    )
    parser.add_argument(
        "--source", type=str, required=True,
        choices=["colmap", "vggt"],
        help="Source reconstruction type"
    )
    parser.add_argument(
        "--iterations", type=int, default=30000,
        help="Number of training iterations"
    )
    parser.add_argument(
        "--save_iterations", type=int, nargs="+",
        default=[7000, 15000],
        help="Additional iterations to save model"
    )
    parser.add_argument(
        "--checkpoint_iterations", type=int, nargs="+",
        default=[],
        help="Iterations to save checkpoints"
    )
    parser.add_argument(
        "--use_depth", action="store_true",
        help="Use depth supervision (if depth maps available)"
    )
    parser.add_argument(
        "--depth_weight_init", type=float, default=0.1,
        help="Initial depth loss weight"
    )
    parser.add_argument(
        "--depth_weight_final", type=float, default=0.01,
        help="Final depth loss weight"
    )
    parser.add_argument(
        "--enable_viewer", action="store_true",
        help="Enable GUI viewer (requires display)"
    )
    parser.add_argument(
        "--build_hierarchy", action="store_true",
        help="Build hierarchy.hier from trained PLY "
             "(requires compiled gaussianhierarchy)"
    )
    parser.add_argument(
        "--post_opt", action="store_true",
        help="Run post-optimization on hierarchy "
             "(requires --build_hierarchy)"
    )

    args = parser.parse_args()

    if args.post_opt and not args.build_hierarchy:
        print("Warning: --post_opt requires --build_hierarchy. "
              "Ignoring --post_opt.")
        args.post_opt = False

    scene_dir = Path(args.scene_dir).resolve()

    try:
        validate_source(scene_dir, args.source)
        train_hier_3dgs(
            scene_dir=scene_dir,
            source=args.source,
            iterations=args.iterations,
            save_iterations=args.save_iterations,
            checkpoint_iterations=args.checkpoint_iterations,
            use_depth=args.use_depth,
            depth_weight_init=args.depth_weight_init,
            depth_weight_final=args.depth_weight_final,
            disable_viewer=not args.enable_viewer,
            build_hierarchy=args.build_hierarchy,
            post_opt=args.post_opt,
        )
    except Exception as exc:
        print(f"\nError: {exc}", file=sys.stderr)
        import traceback
        traceback.print_exc()
        sys.exit(1)


if __name__ == "__main__":
    main()
