#!/usr/bin/env python
"""
Step 3: Train HIER 3DGS from COLMAP/VGGT reconstruction

This script trains a Hierarchical 3D Gaussian Splatting model from the sparse reconstruction
generated by COLMAP (Step 1) or VGGT (Step 2).

Key features:
- Single-scene training (no chunking required)
- Directly uses VGGT poses and intrinsics for alignment
- Optionally builds hierarchy (.hier) from trained PLY if GaussianHierarchyCreator is available
- Falls back to standard PLY output if hierarchy tools are not compiled

Usage:
    # Train from COLMAP
    python 03_train_hier.py --scene_dir /path/to/scene --source colmap --iterations 30000

    # Train from VGGT (no chunking, uses VGGT camera poses directly)
    python 03_train_hier.py --scene_dir /path/to/scene --source vggt --iterations 30000

    # Train + build hierarchy + post-optimize (requires compiled gaussianhierarchy)
    python 03_train_hier.py --scene_dir /path/to/scene --source vggt --iterations 30000 --build_hierarchy --post_opt

Output structure:
    scene_dir/
    ├── 3dgs_result/          # 3DGS training output
    │   ├── model/
    │   │   ├── point_cloud/iteration_30000/point_cloud.ply   # Trained 3DGS (PLY)
    │   │   ├── hierarchy.hier      # (if --build_hierarchy and creator available)
    │   │   ├── hierarchy.hier_opt  # (if --post_opt)
    │   │   ├── final.hier          # Symlink/copy to final output
    │   │   └── ckpt/               # Checkpoints
    │   ├── render/                 # Rendered validation images
    │   └── log/                    # Training logs
    └── [colmap_result|vggt_result]/  # Source reconstruction

Requirements:
    - HIER 3DGS submodule: submodule/hier_3dgs/
    - CUDA-capable GPU
    - ~24GB GPU memory for full training
    - (Optional) Compiled gaussianhierarchy submodule for .hier generation
"""

import argparse
import os
import sys
import subprocess
import shutil
import platform
from pathlib import Path
import json

# Add hier_3dgs to path
HIER_3DGS_PATH = Path(__file__).parent.parent / "submodule" / "hier_3dgs"
sys.path.insert(0, str(HIER_3DGS_PATH))


def run_command(cmd, cwd=None, check=True):
    """Run shell command and print output."""
    print(f"Running: {cmd}")
    env = os.environ.copy()
    conda_prefix = env.get("CONDA_PREFIX")
    if conda_prefix:
        conda_lib = os.path.join(conda_prefix, "lib")
        ld_path = env.get("LD_LIBRARY_PATH", "")
        if conda_lib not in ld_path:
            env["LD_LIBRARY_PATH"] = f"{conda_lib}:{ld_path}" if ld_path else conda_lib
    result = subprocess.run(cmd, shell=True, cwd=cwd, capture_output=True, text=True, env=env)
    if result.stdout:
        print(result.stdout)
    if result.stderr:
        print(result.stderr, file=sys.stderr)
    if check and result.returncode != 0:
        raise RuntimeError(f"Command failed with return code {result.returncode}")
    return result


def find_hierarchy_creator():
    """Find the GaussianHierarchyCreator binary if available."""
    os_name = platform.system()
    rel_path = (
        "submodules/gaussianhierarchy/build/Release/GaussianHierarchyCreator.exe"
        if os_name == "Windows"
        else "submodules/gaussianhierarchy/build/GaussianHierarchyCreator"
    )
    candidate = HIER_3DGS_PATH / rel_path
    if candidate.exists():
        return str(candidate)

    # Extra fallback: search PATH
    binary_name = "GaussianHierarchyCreator.exe" if os_name == "Windows" else "GaussianHierarchyCreator"
    for path_dir in os.environ.get("PATH", "").split(os.pathsep):
        fallback = Path(path_dir) / binary_name
        if fallback.exists():
            return str(fallback)

    return None


def prepare_dataset_structure(scene_dir: Path, source: str):
    """
    Prepare dataset structure for HIER 3DGS training.

    HIER 3DGS expects:
        source_path/
        ├── images/
        └── sparse/

    We use {source}_result/ as source_path because VGGT already outputs
    the standard COLMAP-format sparse/ directory.
    """
    input_dir = scene_dir / "input" / "images"
    source_result_dir = scene_dir / f"{source}_result"
    sparse_dir = source_result_dir / "sparse"

    if not input_dir.exists():
        raise FileNotFoundError(f"Input images not found: {input_dir}")

    if not sparse_dir.exists():
        raise FileNotFoundError(
            f"{source.upper()} reconstruction not found: {sparse_dir}\n"
            f"Please run Step {'1' if source == 'colmap' else '2'} first."
        )

    # Symlink images into source_result_dir so train_single.py finds them
    images_link = source_result_dir / "images"
    if not images_link.exists():
        print(f"Creating symlink: {images_link} -> {input_dir}")
        images_link.symlink_to(input_dir.resolve())

    # train_single.py expects COLMAP files under sparse/0/; create symlink if needed
    sparse_zero = sparse_dir / "0"
    if not sparse_zero.exists():
        has_colmap_files = all((sparse_dir / f).exists() for f in ["cameras.bin", "images.bin", "points3D.bin"])
        if has_colmap_files:
            print(f"Creating symlink: {sparse_zero} -> {sparse_dir}")
            sparse_zero.symlink_to(sparse_dir.resolve())

    # Create train/test split files (needed for --eval)
    # Always regenerate based on images actually present in the reconstruction
    # to avoid mismatch when VGGT/Colmap only uses a subset of input images.
    test_txt = sparse_dir / "test.txt"
    train_txt = sparse_dir / "train.txt"
    try:
        from scene.colmap_loader import read_extrinsics_binary
        cam_extrinsics = read_extrinsics_binary(str(sparse_dir / "images.bin"))
        image_names = sorted([cam_extrinsics[k].name for k in cam_extrinsics])
    except Exception as e:
        print(f"Warning: could not read images.bin for train/test split ({e}). Falling back to input_dir listing.")
        image_names = sorted([p.name for p in input_dir.iterdir() if p.suffix.lower() in ['.jpg', '.jpeg', '.png', '.bmp']])
    if image_names:
        test_names = [name for idx, name in enumerate(image_names) if idx % 8 == 0]
        train_names = [name for name in image_names if name not in test_names]
        test_txt.write_text("\n".join(test_names) + "\n")
        train_txt.write_text("\n".join(train_names) + "\n")
        print(f"Created train/test split: {len(train_names)} train, {len(test_names)} test")

    return source_result_dir, sparse_dir


def _build_hierarchy(ply_path: Path, source_path: Path, output_dir: Path, scaffold_dir: Path = None):
    """
    Build hierarchy.hier from a trained PLY using GaussianHierarchyCreator.

    Args:
        ply_path: Path to point_cloud.ply
        source_path: Path to COLMAP/VGGT sparse reconstruction
        output_dir: Directory to write hierarchy.hier
        scaffold_dir: Optional scaffold directory for large scenes
    """
    creator = find_hierarchy_creator()
    if not creator:
        raise FileNotFoundError(
            "GaussianHierarchyCreator not found.\n"
            "Please compile the gaussianhierarchy submodule:\n"
            "  cd submodule/hier_3dgs/submodules/gaussianhierarchy\n"
            "  git submodule update --init --recursive\n"
            "  mkdir build && cd build && cmake .. && make -j"
        )

    args = [str(creator), str(ply_path), str(source_path), str(output_dir)]
    if scaffold_dir and scaffold_dir.exists():
        args.append(str(scaffold_dir))

    cmd = " ".join(args)
    print(f"\n[Hierarchy] Building hierarchy from PLY...")
    run_command(cmd)

    hier_path = Path(output_dir) / "hierarchy.hier"
    if not hier_path.exists():
        raise FileNotFoundError(f"Hierarchy creation failed: {hier_path} not found")

    print(f"  Created hierarchy: {hier_path}")
    return hier_path


def post_optimize_hierarchy(source_path: Path, model_dir: Path, scaffold_dir: Path = None,
                            iterations: int = 15000, disable_viewer: bool = True):
    """Run train_post.py to refine the hierarchy."""
    train_post = HIER_3DGS_PATH / "train_post.py"
    if not train_post.exists():
        raise FileNotFoundError(f"train_post.py not found: {train_post}")

    hier_path = model_dir / "hierarchy.hier"
    if not hier_path.exists():
        raise FileNotFoundError(f"Hierarchy file not found for post-opt: {hier_path}")

    cmd_parts = [
        f"python {train_post}",
        f"-s {source_path}",
        f"-m {model_dir}",
        f"--iterations {iterations}",
        "--feature_lr 0.0005",
        "--opacity_lr 0.01",
        "--scaling_lr 0.001",
        "--save_iterations -1",
        f"--hierarchy {hier_path}",
    ]

    if scaffold_dir and scaffold_dir.exists():
        cmd_parts.append(f"--scaffold_file {scaffold_dir}")

    if disable_viewer:
        cmd_parts.append("--disable_viewer")

    cmd = " ".join(cmd_parts)
    print(f"\n[Hierarchy] Running post-optimization on hierarchy...")
    run_command(cmd, cwd=str(HIER_3DGS_PATH))

    opt_hier = model_dir / "hierarchy.hier_opt"
    if opt_hier.exists():
        print(f"  Post-optimized hierarchy: {opt_hier}")
    return opt_hier


def train_hier_3dgs(scene_dir: Path, source: str, iterations: int = 30000,
                    save_iterations=None, checkpoint_iterations=None,
                    use_depth: bool = False, depth_weight_init: float = 0.1,
                    depth_weight_final: float = 0.01,
                    disable_viewer: bool = True,
                    build_hierarchy: bool = False,
                    post_opt: bool = False):
    """
    Train HIER 3DGS model.

    Args:
        scene_dir: Scene directory
        source: Source type ('colmap' or 'vggt')
        iterations: Number of training iterations
        save_iterations: List of iterations to save model
        checkpoint_iterations: List of iterations to save checkpoint
        use_depth: Whether to use depth supervision
        depth_weight_init: Initial depth loss weight
        depth_weight_final: Final depth loss weight
        disable_viewer: Disable GUI viewer
        build_hierarchy: Whether to build .hier from trained PLY
        post_opt: Whether to run post-optimization on hierarchy

    Returns:
        model_dir: Path to trained model directory
    """
    # Prepare dataset
    source_path, sparse_dir = prepare_dataset_structure(scene_dir, source)

    # Setup output directory
    result_dir = scene_dir / "3dgs_result"
    model_dir = result_dir / "model"
    ckpt_dir = model_dir / "ckpt"
    log_dir = result_dir / "log"

    result_dir.mkdir(parents=True, exist_ok=True)
    model_dir.mkdir(parents=True, exist_ok=True)
    ckpt_dir.mkdir(parents=True, exist_ok=True)
    log_dir.mkdir(parents=True, exist_ok=True)

    # Default save iterations
    if save_iterations is None:
        save_iterations = [7000, 15000, iterations]
    else:
        save_iterations = sorted(set(save_iterations + [iterations]))

    if checkpoint_iterations is None:
        checkpoint_iterations = []

    print(f"\n{'='*70}")
    print(f"HIER 3DGS Training")
    print(f"{'='*70}")
    print(f"Scene: {scene_dir}")
    print(f"Source: {source.upper()}")
    print(f"Iterations: {iterations}")
    print(f"Save at: {save_iterations}")
    print(f"Build hierarchy: {build_hierarchy}")
    print(f"Post-opt hierarchy: {post_opt}")
    print(f"Output: {result_dir}")

    # Check hierarchy creator availability early if requested
    if build_hierarchy:
        creator = find_hierarchy_creator()
        if creator:
            print(f"✓ GaussianHierarchyCreator found: {creator}")
        else:
            print("⚠ GaussianHierarchyCreator not found. Will fall back to PLY output.")
            print("  To enable hierarchy building, compile the submodule.")

    # Build training command
    train_script = HIER_3DGS_PATH / "train_single.py"
    if not train_script.exists():
        raise FileNotFoundError(f"Training script not found: {train_script}")

    cmd_parts = [
        f"python {train_script}",
        f"-s {source_path}",
        f"-m {model_dir}",
        f"--iterations {iterations}",
        f"--save_iterations {' '.join(map(str, save_iterations))}",
    ]

    if checkpoint_iterations:
        cmd_parts.append(f"--checkpoint_iterations {' '.join(map(str, checkpoint_iterations))}")

    if use_depth:
        cmd_parts.append(f"--depth_l1_weight_init {depth_weight_init}")
        cmd_parts.append(f"--depth_l1_weight_final {depth_weight_final}")

    if disable_viewer:
        cmd_parts.append("--disable_viewer")

    cmd_parts.extend(["--eval"])

    cmd = " ".join(cmd_parts)

    # Run training
    print(f"\n[1/2] Starting 3DGS training...")
    try:
        run_command(cmd, cwd=str(HIER_3DGS_PATH))
    except RuntimeError as e:
        print(f"Training failed: {e}", file=sys.stderr)
        raise

    final_ply = model_dir / f"point_cloud/iteration_{iterations}/point_cloud.ply"
    if not final_ply.exists():
        raise FileNotFoundError(f"Expected PLY not found after training: {final_ply}")

    print(f"  Trained PLY: {final_ply}")

    # Build hierarchy if requested and tools available
    hierarchy_built = False
    final_hier_path = None

    if build_hierarchy and find_hierarchy_creator():
        try:
            hier_path = _build_hierarchy(final_ply, source_path, model_dir)
            hierarchy_built = True

            if post_opt:
                opt_hier = post_optimize_hierarchy(source_path, model_dir, iterations=15000, disable_viewer=disable_viewer)
                final_hier_path = opt_hier
            else:
                final_hier_path = hier_path

        except Exception as e:
            print(f"\n⚠ Hierarchy build failed: {e}", file=sys.stderr)
            print("  Falling back to PLY output.")

    # Copy/link final output to standard location
    final_dest = model_dir / "final.hier"
    if hierarchy_built and final_hier_path and final_hier_path.exists():
        shutil.copy2(final_hier_path, final_dest)
        print(f"  Copied final hierarchy to: {final_dest}")
    else:
        # Create a metadata stub pointing to the PLY for downstream compatibility
        fallback_stub = {
            "format": "ply",
            "ply_path": str(final_ply),
            "note": "Hierarchy tools not available; output is standard 3DGS PLY",
            "hierarchy_available": False,
        }
        stub_path = model_dir / "final.json"
        with open(stub_path, "w") as f:
            json.dump(fallback_stub, f, indent=2)
        print(f"  Saved fallback metadata to: {stub_path}")
        print(f"  (Use PLY directly: {final_ply})")

    # Save training config
    config = {
        "source": source,
        "iterations": iterations,
        "save_iterations": save_iterations,
        "use_depth": use_depth,
        "depth_weight_init": depth_weight_init,
        "depth_weight_final": depth_weight_final,
        "source_path": str(source_path),
        "model_path": str(model_dir),
        "hierarchy_built": hierarchy_built,
        "post_optimized": post_opt and hierarchy_built,
    }
    config_path = result_dir / "config.json"
    with open(config_path, "w") as f:
        json.dump(config, f, indent=2)
    print(f"  Saved config to: {config_path}")

    print(f"\n{'='*70}")
    print(f"HIER 3DGS Training Complete!")
    print(f"{'='*70}")
    print(f"Model directory: {model_dir}")
    print(f"  - point_cloud/iteration_{iterations}/point_cloud.ply: Standard 3DGS model")
    if hierarchy_built:
        print(f"  - hierarchy.hier: Hierarchical model")
        if post_opt:
            print(f"  - hierarchy.hier_opt: Post-optimized hierarchical model")
        print(f"  - final.hier: Symlink to final hierarchy output")
    else:
        print(f"  - final.json: Fallback metadata (hierarchy tools unavailable)")
        print(f"\nTo enable hierarchy generation, compile gaussianhierarchy:")
        print(f"  cd {HIER_3DGS_PATH}/submodules/gaussianhierarchy")
        print(f"  git submodule update --init --recursive")
        print(f"  mkdir build && cd build && cmake .. && make -j")

    return model_dir


def validate_source(scene_dir: Path, source: str):
    """Validate that source reconstruction exists and is valid."""
    source_result_dir = scene_dir / f"{source}_result"
    sparse_dir = source_result_dir / "sparse"

    if not source_result_dir.exists():
        raise FileNotFoundError(
            f"{source.upper()} result directory not found: {source_result_dir}\n"
            f"Please run Step {'1' if source == 'colmap' else '2'} first."
        )

    if not sparse_dir.exists():
        raise FileNotFoundError(f"Sparse reconstruction not found: {sparse_dir}")

    required_files = ["cameras.bin", "images.bin", "points3D.bin"]
    for fname in required_files:
        fpath = sparse_dir / fname
        if not fpath.exists():
            raise FileNotFoundError(f"Missing required file: {fpath}")

    print(f"✓ Valid {source.upper()} reconstruction found")
    print(f"  - cameras.bin: Camera intrinsics")
    print(f"  - images.bin: Camera poses")
    print(f"  - points3D.bin: Sparse 3D points")

    return True


def main():
    parser = argparse.ArgumentParser(description="Train HIER 3DGS from COLMAP or VGGT")
    parser.add_argument("--scene_dir", type=str, required=True,
                        help="Scene directory containing input/images/ and [colmap|vggt]_result/sparse/")
    parser.add_argument("--source", type=str, required=True,
                        choices=["colmap", "vggt"],
                        help="Source reconstruction type")
    parser.add_argument("--iterations", type=int, default=30000,
                        help="Number of training iterations")
    parser.add_argument("--save_iterations", type=int, nargs="+",
                        default=[7000, 15000],
                        help="Additional iterations to save model")
    parser.add_argument("--checkpoint_iterations", type=int, nargs="+",
                        default=[],
                        help="Iterations to save checkpoints")
    parser.add_argument("--use_depth", action="store_true",
                        help="Use depth supervision (if depth maps available)")
    parser.add_argument("--depth_weight_init", type=float, default=0.1,
                        help="Initial depth loss weight")
    parser.add_argument("--depth_weight_final", type=float, default=0.01,
                        help="Final depth loss weight")
    parser.add_argument("--enable_viewer", action="store_true",
                        help="Enable GUI viewer (requires display)")
    parser.add_argument("--build_hierarchy", action="store_true",
                        help="Build hierarchy.hier from trained PLY (requires compiled gaussianhierarchy)")
    parser.add_argument("--post_opt", action="store_true",
                        help="Run post-optimization on hierarchy (requires --build_hierarchy)")

    args = parser.parse_args()

    if args.post_opt and not args.build_hierarchy:
        print("Warning: --post_opt requires --build_hierarchy. Ignoring --post_opt.")
        args.post_opt = False

    scene_dir = Path(args.scene_dir).resolve()

    try:
        validate_source(scene_dir, args.source)
        train_hier_3dgs(
            scene_dir=scene_dir,
            source=args.source,
            iterations=args.iterations,
            save_iterations=args.save_iterations,
            checkpoint_iterations=args.checkpoint_iterations,
            use_depth=args.use_depth,
            depth_weight_init=args.depth_weight_init,
            depth_weight_final=args.depth_weight_final,
            disable_viewer=not args.enable_viewer,
            build_hierarchy=args.build_hierarchy,
            post_opt=args.post_opt,
        )
    except Exception as e:
        print(f"\nError: {e}", file=sys.stderr)
        import traceback
        traceback.print_exc()
        sys.exit(1)


if __name__ == "__main__":
    main()
